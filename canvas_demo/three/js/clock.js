function Clock(){
    var canvas =document.createElement("canvas");
    canvas.setAttribute("width",400);
    canvas.setAttribute("height",400);
    document.body.appendChild(canvas);
    var ctx=canvas.getContext("2d");
    ctx.fillStyle="#FEA500";
    ctx.fillRect(0,0,400,400);
    ctx.beginPath();
    ctx.strokeStyle="#000";
    ctx.lineWidth=4;
    ctx.arc(200,200,170,0,Math.PI*2);
    ctx.stroke();
    ctx.beginPath();
    ctx.fillStyle="#fff";
    ctx.arc(200,200,168,0,Math.PI*2);
    ctx.fill();
    ctx.beginPath();
    ctx.strokeStyle="#FFBC43";
    ctx.lineWidth=16;
    ctx.setLineDash([2,Math.PI/6*146]);
    ctx.arc(200,200,150,0,Math.PI*2);
    ctx.stroke();
    ctx.beginPath();
    ctx.setLineDash([]);
    ctx.lineWidth=6;
    ctx.strokeStyle="#000";
    ctx.moveTo(200,200+20);
    ctx.lineTo(200,80);
    ctx.stroke();
    ctx.beginPath();
    ctx.lineWidth=10;
    ctx.strokeStyle="#000";
    ctx.moveTo(200,200+20);
    ctx.lineTo(200,100);
    ctx.stroke();
    ctx.beginPath();
    ctx.lineWidth=3;
    ctx.strokeStyle="red";
    ctx.moveTo(200,200+20);
    ctx.lineTo(200,40);
    ctx.stroke();
    ctx.beginPath();
    ctx.strokeStyle="#CE8B00";
    ctx.lineWidth=3;
    ctx.arc(200,200,5,0,Math.PI*2);
    ctx.stroke();
    ctx.beginPath();
    ctx.fillStyle="red";
    ctx.arc(200,200,4,0,Math.PI*2);
    ctx.fill();   
}
Clock();